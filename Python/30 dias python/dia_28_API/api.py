# API

'''
API significa Interfaz de programación de aplicaciones.

Las API web son las interfaces definidas a través de las cuales ocurren las interacciones entre una empresa y las aplicaciones que usan sus activos

En el contexto del desarrollo web, una API se define como un conjunto de especificaciones, como los mensajes de solicitud del Protocolo de transferencia de hipertexto (HTTP), junto con una definición de la estructura de los mensajes de respuesta, generalmente en un XML o una notación de objetos de JavaScript (JSON).
'''

# HTTP(Hypertext Transfer Protocol)
'''
HTTP es un protocolo de comunicación establecido entre un cliente y un servidor. Un cliente en este caso es un navegador y un servidor es el lugar donde accede a los datos. HTTP es un protocolo de red utilizado para entregar recursos que podrían ser archivos en la World Wide Web, ya sean archivos HTML, archivos de imágenes, resultados de consultas, scripts u otros tipos de archivos.

Un navegador es un cliente HTTP porque envía solicitudes a un servidor HTTP (servidor web), que luego envía respuestas al cliente.
'''
